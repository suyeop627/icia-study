USE devdb;
-- 테이블 삭제
-- 테이블 삭제 시 외래키를 사용하는 테이블부터 먼저 삭제	키워드 if exists A : A가 있으면키워드 실행
DROP TABLE IF EXISTS SALGRADE;
DROP TABLE IF EXISTS EMP;
DROP TABLE IF EXISTS DEPT;
-- 테이블 생성
-- 테이블 생성 시 외래키를 제공하는 테이블을 먼저 생성		키워드 if not exists A :A가 없으면 키워드 실행
CREATE TABLE IF NOT EXISTS DEPT (
    DEPTNO DECIMAL(2),
    DNAME VARCHAR(14),
    LOC VARCHAR(13),
    CONSTRAINT PK_DEPT PRIMARY KEY (DEPTNO) 
);
CREATE TABLE IF NOT EXISTS EMP (
    EMPNO DECIMAL(4),
    ENAME VARCHAR(10),
    JOB VARCHAR(9),
    MGR DECIMAL(4),
    HIREDATE DATE,
    SAL DECIMAL(7,2),
    COMM DECIMAL(7,2),
    DEPTNO DECIMAL(2),
    CONSTRAINT PK_EMP PRIMARY KEY (EMPNO),
    CONSTRAINT FK_DEPTNO FOREIGN KEY (DEPTNO) REFERENCES DEPT(DEPTNO)
);
CREATE TABLE IF NOT EXISTS SALGRADE ( 
    GRADE TINYINT,
    LOSAL SMALLINT,
    HISAL SMALLINT 
);
